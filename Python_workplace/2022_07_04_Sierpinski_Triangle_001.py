#1
import turtle as a

def tree ( br_len ) :
    if br_len <= 5 :
                       
        return
    new_len = br_len*0.7
    a.fd(br_len)
    a.rt(20)
    tree (new_len)
    a.lt(40)
    tree (new_len)
    a.rt(20)
    a.backward(br_len)

    #함수종료
a.speed(0)
a.lt(90)
tree (70)


